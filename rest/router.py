from rest_framework.routers import DefaultRouter

from rest.loader import load_viewsets

def get_router():
    router = DefaultRouter(trailing_slash=False)

    for args in load_viewsets("RESTAPI_VIEWSETS"):
        router.register(*args)

    return router

ROUTER = get_router()
